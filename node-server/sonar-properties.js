module.exports = {
    sonarPropertiesText(projectKey) {

        const sonar_properties = `# must be unique in a given SonarQube instance\n
            sonar.projectKey=${projectKey}\n
            # this is the name and version displayed in the SonarQube UI. Was mandatory prior to SonarQube 6.1.\n
            sonar.projectName=${projectKey}\n
            sonar.projectVersion=1.0\n
            sonar.language=java\n
            \n
            # Path is relative to the sonar-project.properties file. Replace "\"" by "/" on Windows.\n
            # This property is optional if sonar.modules is set. \n
            sonar.sources=.\n
            \n
            sonar.java.binaries = src/main/java\n
            \n
            # Encoding of the source code. Default is default system encoding\n
            sonar.sourceEncoding=UTF-8\n
            \n
            # JUnit report generated by run-sonar.sh is stored in sonar-reports/TEST-report.xml\n
            # Change it only if you generate the file on your own\n
            # The XML files have to be prefixed by TEST- otherwise they are not processed\n
            sonar.junit.reportPaths=report/\n
            \n
            sonar.login=admin\n
            sonar.password=admin\n`

        return sonar_properties
    }
}

